CREATE TABLE IF NOT EXISTS vocational_training_enrollments (
    id INT AUTO_INCREMENT PRIMARY KEY,
    inmate_id INT NOT NULL,
    program_name VARCHAR(100) NOT NULL,
    skill_category VARCHAR(100) NOT NULL,
    training_center VARCHAR(120) NOT NULL,
    instructor_name VARCHAR(120) NOT NULL,
    enrollment_date DATE NOT NULL,
    completion_status VARCHAR(30) NOT NULL,
    progress_percentage DECIMAL(5,2) NOT NULL DEFAULT 0.00,
    assessment_score DECIMAL(5,2) NULL,
    certification_eligible BOOLEAN NOT NULL DEFAULT FALSE,
    created_by INT NOT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT fk_vocational_training_inmate_id FOREIGN KEY (inmate_id) REFERENCES inmates(id),
    CONSTRAINT fk_vocational_training_created_by FOREIGN KEY (created_by) REFERENCES users(id),
    CONSTRAINT chk_vocational_program_name CHECK (program_name IN ('Carpentry', 'Welding', 'Tailoring', 'Agriculture', 'ICT Training', 'Plumbing', 'Electrical Installation', 'Art & Craft')),
    CONSTRAINT chk_vocational_completion_status CHECK (completion_status IN ('ENROLLED', 'IN_PROGRESS', 'COMPLETED', 'WITHDRAWN')),
    CONSTRAINT chk_vocational_progress_percentage CHECK (progress_percentage >= 0 AND progress_percentage <= 100),
    CONSTRAINT chk_vocational_assessment_score CHECK (assessment_score IS NULL OR (assessment_score >= 0 AND assessment_score <= 100)),
    INDEX idx_vocational_inmate_id (inmate_id),
    INDEX idx_vocational_program_name (program_name),
    INDEX idx_vocational_completion_status (completion_status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS counseling_sessions (
    id INT AUTO_INCREMENT PRIMARY KEY,
    inmate_id INT NOT NULL,
    counselor_name VARCHAR(120) NOT NULL,
    session_type VARCHAR(50) NOT NULL,
    session_date DATE NOT NULL,
    session_notes TEXT NOT NULL,
    behavioral_observation TEXT NOT NULL,
    risk_level VARCHAR(20) NOT NULL,
    follow_up_required BOOLEAN NOT NULL DEFAULT FALSE,
    created_by INT NOT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_counseling_sessions_inmate_id FOREIGN KEY (inmate_id) REFERENCES inmates(id),
    CONSTRAINT fk_counseling_sessions_created_by FOREIGN KEY (created_by) REFERENCES users(id),
    CONSTRAINT chk_counseling_session_type CHECK (session_type IN ('Individual Counseling', 'Group Therapy', 'Trauma Counseling', 'Addiction Recovery', 'Anger Management')),
    CONSTRAINT chk_counseling_risk_level CHECK (risk_level IN ('LOW', 'MEDIUM', 'HIGH', 'CRITICAL')),
    INDEX idx_counseling_inmate_id (inmate_id),
    INDEX idx_counseling_session_date (session_date),
    INDEX idx_counseling_risk_level (risk_level)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS behavioral_assessments (
    id INT AUTO_INCREMENT PRIMARY KEY,
    inmate_id INT NOT NULL,
    behavior_score DECIMAL(5,2) NOT NULL,
    behavior_category VARCHAR(20) NOT NULL,
    observation_notes TEXT NOT NULL,
    incident_count INT NOT NULL DEFAULT 0,
    improvement_level VARCHAR(50) NOT NULL,
    assessed_by INT NOT NULL,
    assessment_date DATE NOT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_behavioral_assessments_inmate_id FOREIGN KEY (inmate_id) REFERENCES inmates(id),
    CONSTRAINT fk_behavioral_assessments_assessed_by FOREIGN KEY (assessed_by) REFERENCES users(id),
    CONSTRAINT chk_behavior_score CHECK (behavior_score >= 0 AND behavior_score <= 100),
    CONSTRAINT chk_behavior_category CHECK (behavior_category IN ('Excellent', 'Good', 'Fair', 'Poor', 'Violent')),
    CONSTRAINT chk_behavior_incident_count CHECK (incident_count >= 0),
    INDEX idx_behavioral_inmate_id (inmate_id),
    INDEX idx_behavioral_category (behavior_category),
    INDEX idx_behavioral_assessment_date (assessment_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

