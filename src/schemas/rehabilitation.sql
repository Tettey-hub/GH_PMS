CREATE TABLE IF NOT EXISTS work_assignments (
    id INT AUTO_INCREMENT PRIMARY KEY,
    inmate_id INT NOT NULL,
    work_type VARCHAR(60) NOT NULL,
    assignment_location VARCHAR(120) NOT NULL,
    supervisor_name VARCHAR(120) NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NULL,
    performance_rating DECIMAL(5,2) NULL,
    attendance_record TEXT NOT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_work_assignments_inmate_id FOREIGN KEY (inmate_id) REFERENCES inmates(id),
    CONSTRAINT chk_work_type CHECK (work_type IN ('Kitchen Duties', 'Farming', 'Cleaning Services', 'Maintenance Work', 'Construction Assistance')),
    CONSTRAINT chk_work_performance_rating CHECK (performance_rating IS NULL OR (performance_rating >= 0 AND performance_rating <= 100)),
    CONSTRAINT chk_work_date_order CHECK (end_date IS NULL OR end_date >= start_date),
    INDEX idx_work_assignments_inmate_id (inmate_id),
    INDEX idx_work_assignments_work_type (work_type),
    INDEX idx_work_assignments_start_date (start_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS skill_certifications (
    id INT AUTO_INCREMENT PRIMARY KEY,
    inmate_id INT NOT NULL,
    certification_name VARCHAR(120) NOT NULL,
    skill_area VARCHAR(100) NOT NULL,
    issuing_authority VARCHAR(120) NOT NULL,
    issue_date DATE NOT NULL,
    certificate_number VARCHAR(80) NOT NULL UNIQUE,
    grade VARCHAR(30) NOT NULL,
    validity_status VARCHAR(30) NOT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_skill_certifications_inmate_id FOREIGN KEY (inmate_id) REFERENCES inmates(id),
    CONSTRAINT chk_skill_certification_grade CHECK (grade IN ('Basic', 'Intermediate', 'Advanced', 'Professional')),
    CONSTRAINT chk_skill_certification_validity CHECK (validity_status IN ('VALID', 'EXPIRED', 'REVOKED')),
    INDEX idx_skill_certifications_inmate_id (inmate_id),
    INDEX idx_skill_certifications_skill_area (skill_area),
    INDEX idx_skill_certifications_issue_date (issue_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS post_release_followups (
    id INT AUTO_INCREMENT PRIMARY KEY,
    inmate_id INT NOT NULL,
    release_date DATE NOT NULL,
    follow_up_date DATE NOT NULL,
    employment_status VARCHAR(80) NOT NULL,
    housing_status VARCHAR(80) NOT NULL,
    reintegration_score DECIMAL(5,2) NOT NULL,
    recidivism_risk_level VARCHAR(20) NOT NULL,
    notes TEXT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_post_release_followups_inmate_id FOREIGN KEY (inmate_id) REFERENCES inmates(id),
    CONSTRAINT chk_post_release_reintegration_score CHECK (reintegration_score >= 0 AND reintegration_score <= 100),
    CONSTRAINT chk_post_release_risk_level CHECK (recidivism_risk_level IN ('LOW', 'MEDIUM', 'HIGH', 'CRITICAL')),
    CONSTRAINT chk_post_release_followup_date CHECK (follow_up_date >= release_date),
    INDEX idx_post_release_inmate_id (inmate_id),
    INDEX idx_post_release_follow_up_date (follow_up_date),
    INDEX idx_post_release_risk_level (recidivism_risk_level)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
